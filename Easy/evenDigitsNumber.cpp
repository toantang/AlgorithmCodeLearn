int ans = 0;

bool checkEvenDigitNumber(int a) {
    if (a <= 0 ) {
        return false;
    }
    else if (a < 10 || (100 <= a && a < 1000) || (10000 <= a && a < 100000) || (1000000 <= a && a < 10000000)) {
        return false;
    }
    return true;
}

void evenDigitNumber(vector<int> v) {
    int len = v.size();
    for (int i = 0; i < len; i++) {
        cout << v[i] << " ";
        if(checkEvenDigitNumber(v[i]) == true) {
            ans++;
        }
    }
}

