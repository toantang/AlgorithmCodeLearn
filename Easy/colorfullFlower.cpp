int colorfulFlowere(vector<int> arr) {
    int ans = 0;
    int cnt = 1;
    int n = arr.size();
    if (n == 0) return 0;
    if (n == 1) return 1;
    for (int i = 0; i < n - 1; i++) {
        if (arr[i+1] == arr[i]) {
            cnt++;
        }
        else {
            ans = max(ans, cnt);
            cnt = 1;
        }
    }

    if (cnt == n) {
        return cnt;
    }
    else {
        return ans;
    }
}

